async function restore() {
  console.log('Restoring HXI Eats expense account deposits...\n');

  // Verify account exists
  const account = await prisma.expenseAccounts.findUnique({ where: { id: ACCOUNT_ID } });
  if (!account) {
    throw new Error('Account not found: ' + ACCOUNT_ID);
  }
  console.log('Account:', account.accountName, '(' + account.accountNumber + ')');
  console.log('Current stored balance:', Number(account.balance));

  // Upsert each deposit (safe to re-run)
  let restored = 0;
  for (const dep of deposits) {
    const existing = await prisma.expenseAccountDeposits.findUnique({ where: { id: dep.id } });
    if (existing) {
      console.log('  SKIP (already exists):', dep.id, '$' + dep.amount);
      continue;
    }
    await prisma.expenseAccountDeposits.create({ data: dep });
    restored++;
    console.log('  RESTORED:', dep.id, '$' + dep.amount, dep.depositDate.toISOString().slice(0,10));
  }

  // Recalculate correct balance from all deposits minus all payments
  const allDeposits = await prisma.expenseAccountDeposits.findMany({ where: { expenseAccountId: ACCOUNT_ID } });
  const allPayments = await prisma.expenseAccountPayments.findMany({ where: { expenseAccountId: ACCOUNT_ID } });
  const totalDeposits = allDeposits.reduce((s, d) => s + Number(d.amount), 0);
  const totalPayments = allPayments.reduce((s, p) => s + Number(p.amount), 0);
  const correctBalance = totalDeposits - totalPayments;

  console.log('\nDeposits total:', totalDeposits.toFixed(2));
  console.log('Payments total:', totalPayments.toFixed(2));
  console.log('Correct balance:', correctBalance.toFixed(2));
  console.log('Stored balance was:', Number(account.balance).toFixed(2));

  if (Math.abs(Number(account.balance) - correctBalance) > 0.01) {
    await prisma.expenseAccounts.update({
      where: { id: ACCOUNT_ID },
      data: { balance: correctBalance }
    });
    console.log('\nBalance updated to:', correctBalance.toFixed(2));
  } else {
    console.log('\nBalance was already correct, no update needed.');
  }

  console.log('\nDone. Restored', restored, 'deposit(s).');
}

restore()
  .then(() => prisma.$disconnect())
  .catch(e => { console.error(e); prisma.$disconnect(); process.exit(1); });
