'use client'

import { useState, useEffect } from 'react'

interface DepositDetail {
  id: string
  amount: number
  depositDate: string
  sourceType: string
  autoGeneratedNote: string | null
  autoTransferSource: string | null
  manualNote: string | null
  transactionType: string | null
  sourceBusiness: { id: string; name: string; type: string } | null
  createdBy: { id: string; name: string; email: string } | null
  // Transfer link
  sourceTransferPaymentId: string | null
  sourceTransferAccountId: string | null
}

function Row({ label, value }: { label: string; value: string }) {
  return (
    <div className="flex gap-3">
      <span className="text-xs text-gray-400 w-28 shrink-0 pt-0.5">{label}</span>
      <span className="text-sm text-primary break-words">{value}</span>
    </div>
  )
}

function sourceTypeLabel(sourceType: string): string {
  switch (sourceType) {
    case 'CASH': return '💵 Cash'
    case 'BANK_TRANSFER': return '🏦 Bank Transfer'
    case 'ACCOUNT_TRANSFER': return '↩ Account Transfer'
    case 'BUSINESS': return '🏢 Business Funding'
    case 'LOAN_RECEIVED': return '🤝 Loan Received'
    case 'LOAN_REPAYMENT': return '🔄 Loan Repayment'
    case 'PAYROLL_FUNDING': return '💼 Payroll Funding'
    case 'TRANSFER_RETURN': return '↩️ Transfer Return'
    case 'PETTY_CASH': return '🪙 Petty Cash'
    case 'WIFI_TOKEN_SALE': return '📡 WiFi Portal Sale'
    case 'R710_TOKEN_SALE': return '📶 R710 WiFi Sale'
    default: return sourceType.charAt(0) + sourceType.slice(1).toLowerCase().replace(/_/g, ' ')
  }
}

export function DepositDetailModal({
  isOpen,
  onClose,
  accountId,
  depositId,
}: {
  isOpen: boolean
  onClose: () => void
  accountId: string
  depositId: string
}) {
  const [deposit, setDeposit] = useState<DepositDetail | null>(null)
  const [loading, setLoading] = useState(false)

  useEffect(() => {
    if (!isOpen || !accountId || !depositId) { setDeposit(null); return }
    setLoading(true)
    fetch(`/api/expense-account/${accountId}/deposits/${depositId}`, { credentials: 'include' })
      .then(r => r.ok ? r.json() : null)
      .then(d => setDeposit(d?.data?.deposit ?? null))
      .catch(() => {})
      .finally(() => setLoading(false))
  }, [isOpen, accountId, depositId])

  if (!isOpen) return null

  const isAutoXferIn = deposit?.sourceType === 'ACCOUNT_TRANSFER'
  const note = deposit?.manualNote || deposit?.autoGeneratedNote || null

  return (
    <div
      className="fixed inset-0 bg-black/60 backdrop-blur-sm flex items-center justify-center z-[10200] p-4"
      onClick={onClose}
    >
      <div
        className="bg-white dark:bg-gray-900 rounded-xl shadow-2xl border border-border w-full max-w-sm"
        onClick={e => e.stopPropagation()}
      >
        {/* Header */}
        <div className="flex items-center justify-between px-5 py-4 border-b border-border">
          <h3 className="text-base font-semibold text-primary">
            {isAutoXferIn ? '↩ Auto-Transfer In' : '📥 Deposit Details'}
          </h3>
          <button
            onClick={onClose}
            className="text-gray-400 hover:text-gray-600 dark:hover:text-gray-200 text-2xl leading-none w-7 h-7 flex items-center justify-center rounded hover:bg-gray-100 dark:hover:bg-gray-800"
          >
            &times;
          </button>
        </div>

        {/* Body */}
        <div className="px-5 py-4">
          {loading ? (
            <p className="text-sm text-gray-400 py-8 text-center">Loading…</p>
          ) : !deposit ? (
            <p className="text-sm text-gray-400 py-8 text-center">Deposit not found</p>
          ) : (
            <div className="space-y-3">
              <Row label="Amount" value={`$${deposit.amount.toFixed(2)}`} />
              <Row label="Date" value={deposit.depositDate.slice(0, 10)} />
              <Row label="Source" value={sourceTypeLabel(deposit.sourceType)} />

              {/* Auto-transfer: show source account with link */}
              {isAutoXferIn && deposit.autoTransferSource && (
                <div className="flex gap-3">
                  <span className="text-xs text-gray-400 w-28 shrink-0 pt-0.5">From Account</span>
                  {deposit.sourceTransferAccountId && deposit.sourceTransferPaymentId ? (
                    <a
                      href={`/expense-accounts/${deposit.sourceTransferAccountId}/payments/${deposit.sourceTransferPaymentId}`}
                      className="text-sm text-sky-600 dark:text-sky-400 hover:underline font-medium"
                      onClick={onClose}
                    >
                      ↩ {deposit.autoTransferSource}
                    </a>
                  ) : (
                    <span className="text-sm text-sky-700 dark:text-sky-400 font-medium">
                      ↩ {deposit.autoTransferSource}
                    </span>
                  )}
                </div>
              )}

              {/* Non-auto-transfer: show source business if present */}
              {!isAutoXferIn && deposit.sourceBusiness && (
                <Row label="From Business" value={deposit.sourceBusiness.name} />
              )}

              {note && <Row label="Notes" value={note} />}
              {deposit.transactionType && deposit.transactionType !== 'STANDARD' && (
                <Row label="Type" value={deposit.transactionType} />
              )}
              {deposit.createdBy && <Row label="Recorded by" value={deposit.createdBy.name} />}
            </div>
          )}
        </div>

        {/* Footer */}
        <div className="px-5 py-3 border-t border-border flex justify-between items-center">
          {deposit && !isAutoXferIn && (
            <a
              href={`/expense-accounts/${accountId}/deposits/${depositId}`}
              onClick={onClose}
              className="text-xs text-blue-600 dark:text-blue-400 hover:underline"
            >
              View full details →
            </a>
          )}
          <button
            onClick={onClose}
            className="ml-auto px-4 py-1.5 text-sm bg-gray-100 dark:bg-gray-800 hover:bg-gray-200 dark:hover:bg-gray-700 text-primary rounded-md"
          >
            Close
          </button>
        </div>
      </div>
    </div>
  )
}
